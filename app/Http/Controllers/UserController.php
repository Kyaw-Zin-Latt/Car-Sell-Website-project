     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\User  $user
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, User $user)
    {
        $request->validate([
            "name" => "required|min:3",
            "email" => "required|email|unique:users,email,".$user->id,
            "photo" => "required|max:1000|mimes:jpg,png,jpeg"
        ]);

        $file = "profile_".uniqid().".".$request->file("photo")->extension();
        $request->file("photo")->storeAs("public/profile/",$file);

        $user->name = $request->name;
        $user->email = $request->email;
        $user->photo = $file;
        $user->update();

        return redirect()->route("user-index")->with(["message" => "Your Profile is updated successfully"]);
    }

